MODELS_ORDER = ["blip2", "llava", "qwen"]
MODEL_LABELS = {"blip2": "BLIP2", "llava": "LLaVA", "qwen": "Qwen"}

def _ensure_numpy(v):
    return np.asarray(list(v), dtype=float)

def compute_curve_B1_normalized_turns(samples, nbins=10):
    """
    B1：归一化 turn（相对进程对齐）
    - 对每个样本：把 turn 索引 t 映射到 τ=t/T（T 为该样本总轮数）
    - 在 τ∈(0,1] 上做分箱（nbins），每 bin 求各模型的平均 Answer-OK
    - 返回：dict(model -> DataFrame with cols [bin_center, mean, N])
    """
    results = {m: [] for m in MODELS_ORDER}
    # 暂存每个模型每个 bin 的样本值
    bins = np.linspace(0, 1, nbins+1)
    bin_centers = (bins[:-1] + bins[1:]) / 2.0
    bucket_vals = {m: {b: [] for b in range(nbins)} for m in MODELS_ORDER}

    for s in samples:
        T = len(next(iter(s["series"].values())))
        taus = [(i+1)/T for i in range(T)]
        # 每个 turn 找到所在 bin
        bin_idx = np.digitize(taus, bins, right=True) - 1
        bin_idx = np.clip(bin_idx, 0, nbins-1)
        for m in MODELS_ORDER:
            arr = _ensure_numpy(s["series"][m])
            for i, val in enumerate(arr):
                bucket_vals[m][int(bin_idx[i])].append(float(val))

    # 聚合
    out = {}
    for m in MODELS_ORDER:
        rows = []
        for b in range(nbins):
            vals = bucket_vals[m][b]
            if len(vals) == 0:
                mean, N = np.nan, 0
            else:
                mean, N = float(np.mean(vals)), len(vals)
            rows.append(dict(bin_center=float(bin_centers[b]), mean=mean, N=N))
        out[m] = pd.DataFrame(rows)
    return out

def compute_curve_D_revisit(samples, max_k=6):
    """
    D：Revisit 曲线（以“第 k 次重访该图像”对齐）
    - 对每个样本的每一张被提及的图，按 turn_to_images 找到该图像的出现序列
    - 第 k 次出现所在 turn 的 Answer-OK，汇总跨图像/跨样本的均值
    """
    buckets = {m: {k: [] for k in range(1, max_k+1)} for m in MODELS_ORDER}
    for s in samples:
        t2imgs = s["turn_to_images"]
        # 为每张图片收集出现 turn
        appear = defaultdict(list)  # img_id -> [turn_index]
        for t, imgs in enumerate(t2imgs, start=1):
            for img in imgs:
                appear[img].append(t)
        # 汇总第 k 次
        for img, turns in appear.items():
            for k_idx, t in enumerate(turns, start=1):
                if k_idx > max_k:
                    break
                for m in MODELS_ORDER:
                    arr = s["series"][m]
                    if 1 <= t <= len(arr):
                        buckets[m][k_idx].append(float(arr[t-1]))

    # 聚合
    out = {}
    for m in MODELS_ORDER:
        rows = []
        for k in range(1, max_k+1):
            vals = buckets[m][k]
            mean = float(np.mean(vals)) if len(vals) else np.nan
            N = len(vals)
            rows.append(dict(k=k, mean=mean, N=N))
        out[m] = pd.DataFrame(rows)
    return out

def compute_curve_B2_event_anchor(samples, max_delta=8):
    """
    B2：事件锚定对齐（以“首次引入该图像”的 turn 为锚点 t0，横轴是 Δ=t-t0）
    - 收集 t0、t0+1、t0+2 … 的 Answer-OK（只要该Δ存在于该样本的对话范围内）
    - 和 Revisit 不同：这里不要求“再次问到该图像”，而是看时间推进对答案质量的影响
    """
    buckets = {m: {d: [] for d in range(0, max_delta+1)} for m in MODELS_ORDER}
    for s in samples:
        T = len(next(iter(s["series"].values())))
        t2imgs = s["turn_to_images"]
        # 找每个图像的 t0
        first_turn = {}
        for t, imgs in enumerate(t2imgs, start=1):
            for img in imgs:
                if img not in first_turn:
                    first_turn[img] = t
        # 以 t0 为锚，取 Δ=0..max_delta
        for img, t0 in first_turn.items():
            for delta in range(0, max_delta+1):
                t = t0 + delta
                if t > T:
                    break
                for m in MODELS_ORDER:
                    arr = s["series"][m]
                    buckets[m][delta].append(float(arr[t-1]))

    out = {}
    for m in MODELS_ORDER:
        rows = []
        for d in range(0, max_delta+1):
            vals = buckets[m][d]
            mean = float(np.mean(vals)) if len(vals) else np.nan
            N = len(vals)
            rows.append(dict(delta=d, mean=mean, N=N))
        out[m] = pd.DataFrame(rows)
    return out

def plot_with_ci(dfmap, xcol, xlabel, title, out_png):
    plt.figure(figsize=(9,5.6))
    for m in MODELS_ORDER:
        df = dfmap[m].copy()
        # 二项近似置信带（可视为“回答为1的比例”）
        p = df["mean"].values
        N = df["N"].values
        se = np.sqrt(np.clip(p*(1-p)/np.maximum(N,1), 0, 1))
        ci = 1.96 * se
        x = df[xcol].values
        plt.plot(x, p, marker='o', label=MODEL_LABELS.get(m, m))
        plt.fill_between(x, np.clip(p-ci, 0, 1), np.clip(p+ci, 1e-9, 1), alpha=0.15)

    plt.ylim(0, 1.02)
    plt.xlabel(xlabel)
    plt.ylabel("Answer-OK rate")
    plt.title(title)
    plt.legend()
    plt.grid(True, alpha=0.3)
    plt.tight_layout()
    plt.savefig(out_png, dpi=150)
    plt.close()

def save_tables(dfmap, out_csv_prefix):
    for m, df in dfmap.items():
        df.to_csv(f"{out_csv_prefix}_{m}.csv", index=False)

def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--out-dir", default="manual_plots", help="输出图片与表的目录")
    ap.add_argument("--nbins", type=int, default=10, help="B1: 归一化 turn 的分箱数")
    ap.add_argument("--max-k", type=int, default=6, help="D: 最大重访次数")
    ap.add_argument("--max-delta", type=int, default=8, help="B2: 事件锚定最大Δ")
    args = ap.parse_args()

    os.makedirs(args.out_dir, exist_ok=True)

    # B1：归一化 turn
    b1 = compute_curve_B1_normalized_turns(SAMPLES, nbins=args.nbins)
    save_tables(b1, os.path.join(args.out_dir, "b1_normturn"))
    plot_with_ci(b1, "bin_center",
                 xlabel="Normalized dialogue progress τ",
                 title="Answer-OK vs Normalized Dialogue Progress (B1)",
                 out_png=os.path.join(args.out_dir, "B1_normalized_turns.png"))

    # D：Revisit
    d = compute_curve_D_revisit(SAMPLES, max_k=args.max_k)
    save_tables(d, os.path.join(args.out_dir, "d_revisit"))
    plot_with_ci(d, "k",
                 xlabel="Revisit index k",
                 title="Answer-OK vs Revisit Index (D)",
                 out_png=os.path.join(args.out_dir, "D_revisit.png"))

    # B2：事件锚定
    b2 = compute_curve_B2_event_anchor(SAMPLES, max_delta=args.max_delta)
    save_tables(b2, os.path.join(args.out_dir, "b2_event_anchor"))
    plot_with_ci(b2, "delta",
                 xlabel="Turns after first mention (Δ = t - t₀)",
                 title="Answer-OK vs Turns Since First Mention (B2)",
                 out_png=os.path.join(args.out_dir, "B2_event_anchor.png"))

    print(f"✅ Done. Figures & tables saved under: {args.out_dir}")

if __name__ == "__main__":
    main()
